void Room::setDirection(char direction, Room* location)
{
	if (direction == 'R')
		this->right = location;
	else if (direction == 'L')
		this->left = location;
	else if (direction == 'U')
		this->upstairs = location;
	else if (direction == 'D')
		this->downstairs = location;
}

